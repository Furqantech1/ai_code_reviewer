                  <button onClick={() => handleCopy(results.review, 'Review')} className="text-xs bg-white dark:bg-slate-800 hover:bg-indigo-50 dark:hover:bg-indigo-600 text-slate-600 dark:text-slate-300 hover:text-indigo-600 dark:hover:text-white px-3 py-1.5 rounded-md border border-slate-200 dark:border-slate-700 transition-colors">Copy</button>
